def finder(n, id, se, w):
    sen = se.split()
    start = -1
    end = -1
    for s in range(len(n)):
        if n[s] == id and (start == -1 and end == -1):
            start = s
            end = s
        elif n[s] == id+1 and start != -1 and end != -1:
            end = s
        elif n[s] != id+1 and start != -1 and end != -1:
            if " ".join(sen[start:end+1]) == w:
                break
            else:
                start = -1
                end = -1
                s -= 1
    return start, end
